#include "libft.h"

int	ft_is_dot(char *name)
{
	char *n;

	if ((n = ft_strrchr(name, '/')))
		++n;
	else
		n = name;
	if (ft_strequ(n, ".") || ft_strequ(n, ".."))
		return (1);
	return (0);
}
